# 高铁座位位置判断程序
# 1. 获取输入并统一转为大写
seat_num = input("请输入高铁座位号：").upper()

# 2. 初始化合法性标记
valid = True

# 3. 拆分排数与座位字母
if len(seat_num) >= 2:
    # 最后一位为字母，前面为排数
    seat_char = seat_num[-1]
    row_str = seat_num[:-1]
    # 校验排数是否为纯数字
    if not row_str.isdigit():
        valid = False
    else:
        row = int(row_str)
        # 校验排数范围1-17
        if row < 1 or row > 17:
            valid = False
    # 校验座位字母是否合法
    if seat_char not in ['A', 'B', 'C', 'D', 'F']:
        valid = False
else:
    # 输入长度过短，非法
    valid = False

# 4. 判断座位位置并输出
if valid:
    if seat_char in ['A', 'F']:
        print("窗口")
    elif seat_char in ['C', 'D']:
        print("过道")
    elif seat_char == 'B':
        print("中间座席")
else:
    print("输入错误")


